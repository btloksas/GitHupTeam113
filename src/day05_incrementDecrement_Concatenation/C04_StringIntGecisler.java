package day05_incrementDecrement_Concatenation;

public class C04_StringIntGecisler {

    public static void main(String[] args) {
        String s1="423";

        int sayi=Integer.parseInt(s1);

        System.out.println(sayi++);

        int a=20;
        int b=40;
        System.out.println(a+b);

        System.out.println(a+" "+b);

    }
}
