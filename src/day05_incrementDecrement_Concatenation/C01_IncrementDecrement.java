public class C01_IncrementDecrement {
    public static void main(String[] args) {

        int a = 20;

        //a=a+3;

        a+=3;

        int b=a;


        System.out.println("a: " + a +", b: "+b);

        a=20;
        b=a;

        a+=3;

        System.out.println("a: " + a +", b: "+b);

        a=20;

        System.out.println("a:" +a);


        a+=5;


        a=20;

        a-=2;

        System.out.println("a:" +a);


    }

}
