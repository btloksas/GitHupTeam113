package day12_forLoops;

public class C01_DikkatEdilecekler {
    public static void main(String[] args) {

        for (int i = 20; i < 10; i++) {

            System.out.print(i + " ");

        }
            for (int i = 20; i < 10; i--)  {

                System.out.print(i + " ");


            }

    }
}
